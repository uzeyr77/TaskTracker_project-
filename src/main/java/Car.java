public class Car {
    int year;
    String model;

    public Car () {
        this.model = "hondizzy";
        this.year = 1994;
    }
    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }
}
